function generateDesigns(total = 90) {
  const designs = [];
  for (let i = 0; i < total; i++) {
    const idx = i + 1;
    const url = `/catalogue/${idx}.jpeg`;
    designs.push({
      id: `D${String(idx).padStart(4, "0")}`,
      name: NAMES[i % NAMES.length],
      imageUrl: url,
      imageUrlLarge: url,
      minMetres: 100
    });
  }
  return designs;
}
